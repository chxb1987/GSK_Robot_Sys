int decodeStringCompare(const char *srcStr, const char *desStr, int len)
{
    int i = 0;
    int val;

    while (*srcStr != 0 && *desStr != 0) {
        val = abs(*srcStr - *desStr);
        if (val != 0 && val != 32) {//大小写
            return *srcStr - *desStr;//返回大小，大于0则大，小于0则小
        }
        srcStr++;
        desStr++;
        i++;
        if (len > 0) {
            if (i == len) {
                return 0;
            }
        }
    }
    if (*srcStr != 0) {
        return 1;
    } else if (*desStr != 0) {
        return -1;
    }

    return 0;
}

