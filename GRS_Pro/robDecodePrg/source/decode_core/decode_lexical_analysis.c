int decodeStringCompareEqual(char *srcStr, char *desStr, int len)
{
    int result = 0;
    int i = 0;
    int val;

    while (i < len) {
        if (*srcStr != 0 && *desStr != 0){
            val = abs(*srcStr - *desStr);
            if (val != 0 && val != 32) {//大小写
                return result;
            }
        } else {
            return result;
        }
        srcStr++;
        desStr++;
        i++;
    }
    result = 1;

    return result;
}

