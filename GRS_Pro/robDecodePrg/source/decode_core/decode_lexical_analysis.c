char *decodeGetOneSegment(char *inputStr, char *outputStr)
{
    char *ptr = NULL;
    int cnt = 0;
    int optFlag = 0;//可选参数斜杠标记
    while (*inputStr != 0) {
        if (*inputStr == ' ' || *inputStr == '\t' || *inputStr == ';' || *inputStr == '\n' || *inputStr == ',' || *inputStr == '\\') {//去掉开头的空格
            if (*inputStr == '\\') {
                optFlag = 1;
            }
            inputStr++;
            continue;
        }
        break;
    }
    if (*inputStr == 0) {//表示到末尾
        return NULL;
    }

    if (optFlag == 1) {
        *outputStr = '\\';
        outputStr++;
    }

    while (*inputStr != 0) {
        if (*inputStr == ' ' || *inputStr == '\t' || *inputStr == ';' || *inputStr == '\n' || *inputStr == ',' || *inputStr == '\\') {//去掉开头的空格
            *outputStr = 0;
            if (cnt != 0) {
                ptr = inputStr;
            }
            return ptr;
        }
        *outputStr = *inputStr;
        inputStr++;
        outputStr++;
        cnt++;
    }
    if (cnt != 0) {
        ptr = inputStr;
    }
    return ptr;
}

