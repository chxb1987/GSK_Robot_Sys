int decode_parse_read_line(char *str[])
{
	char line_str[1024];
	char *pstr = line_str;
	char *tmp_ptr = NULL;
	int read_cnt = 0;
	int pstr_pos = 0;
	int prg_end_flag = 0;//程序段结束有效标志
	int i = 0;

    if(DecodeTaskFileInfoGlobal.pFile != NULL){
        read_cnt = fread(line_str, 1, 1024, DecodeTaskFileInfoGlobal.pFile);
        fgets(line_str, 1024, DecodeTaskFileInfoGlobal.pFile);
		while(*pstr != 0){
			if(*pstr == ' ' || *pstr == '\t' || *pstr == '\r' || *pstr == '\n' || *pstr == ';'){//如果是空格或制表符
				if(*pstr == ';'){//该行程序段结束，或者其后是注释字符，跳过
					prg_end_flag = 1;
				}
				*pstr = 0;
				if(tmp_ptr != NULL){
					strcpy(str[i], tmp_ptr);
					i++;
					tmp_ptr = NULL;
					pstr_pos = 0;
				}
				if(prg_end_flag == 1){//行结束后提前退出
					break;
				}

			}else {
				if(*pstr >= 'a' && *pstr <= 'z'){
					*pstr -= 32;//小写转大写
				}
				if(pstr_pos == 0){
					tmp_ptr = pstr;
				}
				pstr_pos++;
			}
			pstr++;
			read_cnt++;
		}
		str[i] = NULL;
	}
	if(prg_end_flag == 0){//此处要添加界面中的报警，程序段未正常结束
		//此处添加报警代码
	}
    fclose(DecodeTaskFileInfoGlobal.pFile);//测试用，正式版本要注释
	return read_cnt;
}
