int initDecodeShmem(void)
{
    memset(&DecodeShmemInputGlobal, 0, sizeof(decode_shmem_input));
    memset(&DecodeShmemOutputGlobal, 0, sizeof(decode_shmem_output));

    DecodeShmemInputGlobal.startPtr.curModule = -1;
    DecodeShmemInputGlobal.startPtr.curModuleLine = -1;
    DecodeShmemInputGlobal.startPtr.curRoutine = -1;
    DecodeShmemInputGlobal.startPtr.curRoutineLine = -1;

    DecodeShmemOutputGlobal.execPtr.curModule = -1;
    DecodeShmemOutputGlobal.execPtr.curModuleLine = -1;
    DecodeShmemOutputGlobal.execPtr.curRoutine = -1;
    DecodeShmemOutputGlobal.execPtr.curRoutineLine = -1;

    DecodeShmemOutputGlobal.readPtr.curModule = -1;
    DecodeShmemOutputGlobal.readPtr.curModuleLine = -1;
    DecodeShmemOutputGlobal.readPtr.curRoutine = -1;
    DecodeShmemOutputGlobal.readPtr.curRoutineLine = -1;

    return 1;
}

